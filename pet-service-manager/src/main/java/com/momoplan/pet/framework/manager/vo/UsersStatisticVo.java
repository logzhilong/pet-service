public class UsersStatisticVo {
	public String channel;
	public long dailyRegisters;
	public long weeklyRegisters;
	public long monthlyRegister;
	public long dailyUsers;
	public long weeklyUsers;
	public long monthlyUsers;
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public long getDailyRegisters() {
		return dailyRegisters;
	}
	public void setDailyRegisters(long dailyRegisters) {
		this.dailyRegisters = dailyRegisters;
	}
	public long getWeeklyRegisters() {
		return weeklyRegisters;
	}
	public void setWeeklyRegisters(long weeklyRegisters) {
		this.weeklyRegisters = weeklyRegisters;
	}
	public long getMonthlyRegister() {
		return monthlyRegister;
	}
	public void setMonthlyRegister(long monthlyRegister) {
		this.monthlyRegister = monthlyRegister;
	}
	public long getDailyUsers() {
		return dailyUsers;
	}
	public void setDailyUsers(long dailyUsers) {
		this.dailyUsers = dailyUsers;
	}
	public long getWeeklyUsers() {
		return weeklyUsers;
	}
	public void setWeeklyUsers(long weeklyUsers) {
		this.weeklyUsers = weeklyUsers;
	}
	public long getMonthlyUsers() {
		return monthlyUsers;
	}
	public void setMonthlyUsers(long monthlyUsers) {
		this.monthlyUsers = monthlyUsers;
	}
	
}
